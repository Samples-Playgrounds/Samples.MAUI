    // Private
    
    public static string ConvertToFlag(string countryCode)
    {
        if (string.IsNullOrEmpty(countryCode) || countryCode.Length < 2) return string.Empty;

        // Normalize to uppercase to match Unicode flag encoding
        countryCode = countryCode.ToUpperInvariant();

        // Convert each letter to the corresponding regional indicator symbol
        var firstLetter = char.ConvertFromUtf32(0x1F1E6 + (countryCode[0] - 'A'));
        var secondLetter = char.ConvertFromUtf32(0x1F1E6 + (countryCode[1] - 'A'));

        return firstLetter + secondLetter;
    } 
